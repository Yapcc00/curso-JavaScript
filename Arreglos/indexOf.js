/**Si tú quieres saber la posición  índice de un elemento arreglo.
 *  Tú puedes usar el método  indexOf() para obtener eso. 
 * Devuelve el índice de la primera aparición de un elemento en el arreglo.
 *  Si un elemento no se encuentra, el  método indexOf() regresa -1. */

