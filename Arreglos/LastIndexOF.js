/* ay otro método lastIndexOf() esto ayuda a encontrar el índice de la última aparición de un elemento
 en un arreglo. Como indexOf(), lastIndexOf() también regresa -1 cuando el elemento no es encontrado.
*/
const nombres = ['tom', 'alex', 'bob', 'tom'];

nombres.indexOf('tom'); // returns 0
nombres.lastIndexOf('tom'); // returns 3